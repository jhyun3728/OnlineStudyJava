package practicetemplate;

public class Sonata extends Car{
	@Override
	public void turnOn() {
		System.out.println("Sonata 시동을 켰습니다.");
	}

	@Override
	public void start() {
		System.out.println("Sonata 주행을 시작합니다.");
	}

	@Override
	public void stop() {
		System.out.println("Sonata 시동을 켰습니다.");
	}

	@Override
	public void turnOff() {
		System.out.println("Sonata 시동을 켰습니다.");
	}
}
