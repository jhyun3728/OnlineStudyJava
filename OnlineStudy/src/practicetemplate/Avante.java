package practicetemplate;

public class Avante extends Car{

	@Override
	public void turnOn() {
		System.out.println("Avante 시동을 켰습니다.");
	}

	@Override
	public void start() {
		System.out.println("Avante 주행을 시작합니다.");
	}

	@Override
	public void stop() {
		System.out.println("Avante 시동을 켰습니다.");
	}

	@Override
	public void turnOff() {
		System.out.println("Avante 시동을 켰습니다.");
	}

	
}
